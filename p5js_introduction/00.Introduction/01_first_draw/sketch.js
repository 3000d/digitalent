/*
 la fonction setup est celle dans laquelle nous allons
 définir les paramètres de départ de notre sketch
*/
function setup() {
  createCanvas(800,800);
  // dessiner un cercle dans p5js est aussi simple que cela
  ellipse(400,400,100, 100);

  // pour dessiner un carré ou un rectangle 
  rect(70,80, 10,10);
}
